public class questoes_anamenese_POJO {
    int id_questoes, ordem, estado;
    String texto_questao;

    public int getId_questoes() {
        return id_questoes;
    }

    public void setId_questoes(int id_questoes) {
        this.id_questoes = id_questoes;
    }

    public int getOrdem() {
        return ordem;
    }

    public void setOrdem(int ordem) {
        this.ordem = ordem;
    }

    public int getEstado() {
        return estado;
    }

    public void setEstado(int estado) {
        this.estado = estado;
    }

    public String getTexto_questao() {
        return texto_questao;
    }

    public void setTexto_questao(String texto_questao) {
        this.texto_questao = texto_questao;
    }
    
    
}
